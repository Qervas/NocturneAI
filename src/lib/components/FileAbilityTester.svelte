      </div>
      <div class="button-group">
        <button class="btn-secondary" on:click={createTestContent}>
          üìÑ Generate Test Content
        </button>
        <button class="btn-primary" disabled={!testContent.trim()} on:click={testFileWrite}>
          ‚úèÔ∏è Test File Write {hasFileWriter ? '(‚úÖ)' : '(‚ùå)'}
        </button>
      </div>
    {#if writeResult}
      <div class="result">
        <h5>Write Result:</h5>
        <div class="file-info">
          <p><strong>File:</strong> {writeResult.fileName}</p>
          <p><strong>Size:</strong> {writeResult.fileSize} bytes</p>
          <p><strong>Success:</strong> {writeResult.success ? '‚úÖ' : '‚ùå'}</p>
          {#if writeResult.error}
            <p><strong>Error:</strong> {writeResult.error}</p>
          {/if}
          {#if writeResult.downloadUrl}
            <p><strong>Download:</strong> <a href={writeResult.downloadUrl} download={writeResult.fileName} class="download-link">üì• Download File</a></p>
          {/if}
        </div>
      </div>
    {/if}
  </div>

  <!-- Ability Status -->
  <div class="tester-section">
    <h4>‚öôÔ∏è Ability Status</h4>
    <p><strong>Current Agent:</strong> {$selectedAgent || 'None'}</p>
    <p><strong>File Reader:</strong> {hasFileReader ? '‚úÖ Granted' : '‚ùå Not Granted'}</p>
    <p><strong>File Writer:</strong> {hasFileWriter ? '‚úÖ Granted' : '‚ùå Not Granted'}</p>
    <p class="note">üí° Enable/disable file abilities using the skill panel switches above.</p>
    
    <button class="btn-secondary" on:click={() => {
      // Check if abilities are registered
      const allAbilities = abilityManager.getAllAbilities();
      const readAbility = abilityManager.getAbility('read_files');
      const writeAbility = abilityManager.getAbility('write_files');
      
      console.log('üîç All abilities:', allAbilities.map(a => a.id));
      console.log('üîç Read ability:', readAbility);
      console.log('üîç Write ability:', writeAbility);
      
      alert(`üîç Ability Check:\nAll: ${allAbilities.map(a => a.id).join(', ')}\nRead: ${readAbility ? '‚úÖ' : '‚ùå'}\nWrite: ${writeAbility ? '‚úÖ' : '‚ùå'}`);
    }}>
      üîç Check Abilities
    </button>
    
    <button class="btn-secondary" on:click={() => {
      // Manually grant abilities to test
      if ($selectedAgent) {
        const agentId = getAgentFullId($selectedAgent);
        abilityManager.grantAbility(agentId, 'read_files');
        abilityManager.grantAbility(agentId, 'write_files');
        
        // Check if they were granted
        const hasRead = abilityManager.hasAbility(agentId, 'read_files');
        const hasWrite = abilityManager.hasAbility(agentId, 'write_files');
        
        alert(`üîß Manual Grant Test:\nRead: ${hasRead ? '‚úÖ' : '‚ùå'}\nWrite: ${hasWrite ? '‚úÖ' : '‚ùå'}`);
      }
    }}>
      üîß Test Grant
    </button>
  </div>
</div>

